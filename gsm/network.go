func LoadCSVFile(fn string) error {
	f, err := os.Open(fn)
	if err != nil {
		return err
	}
	defer f.Close()
	r := csv.NewReader(f)
	ln := 0
	for {
		ln++
		record, err := r.Read()
		if err != nil {
			if err == io.EOF {
				break
			}
			return errors.Wrapf(err, "CSV reader failed in file %s on line %d", fn, ln)
		}
		if err := LoadCSV(record); err != nil {
			return errors.Wrapf(err, "%s(%d): %+v", fn, ln, err)
		}
	}
	return nil
}

func LoadCSV(record []string) error {
	//columns: MCC,MCC (int),MNC,MNC (int),ISO,Country,Country Code,Network
	//e.g.:
	// 655,1621,01,31,za,South Africa,27,Vodacom
	// 655,1621,02,47,za,South Africa,27,Telkom
	// 655,1621,05,95,za,South Africa,27,Telkom
	// 655,1621,07,127,za,South Africa,27,Cell C
	// 655,1621,10,271,za,South Africa,27,MTN
	// 655,1621,12,303,za,South Africa,27,MTN
	// 655,1621,21,543,za,South Africa,27,Cape Town Metropolitan
	// 655,1621,299,665,za,South Africa,27,Lycamobile
	// 655,1621,38,911,za,South Africa,27,Rain
	// 655,1621,19,415,za,South Africa,27,Rain
	// 655,1621,73,1855,za,South Africa,27,Rain
	// 655,1621,74,1871,za,South Africa,27,Rain
	// 655,1621,06,111,za,South Africa,27,Sentech

	mcc := record[0]
	if mcc == "MCC" {
		return nil //skip over header row
	}
	if !mccRegex.MatchString(mcc) {
		return errors.Errorf("invalid mcc(%s) in %+v", mcc, record)
	}

	mnc := record[2]
	if len(record[2]) > 2 {
		//return errors.Errorf("%s(%d): Invalid MNC(%s) != 2 digits: %+v", fn, line, record[2], record)
		return nil //skip long mnc, e.g. 999 entries in the file to signify fixed lines
	}
	if !mncRegex.MatchString(mnc) {
		return errors.Errorf("invalid mnc(%s) in %+v", mnc, record)
	}

	iso := record[4]
	cName := record[5]
	cc := record[6]
	nName := record[7]

	c, ok := countryByMCC[mcc]
	if !ok {
		//new country
		if o, ok := countryByName[cName]; ok {
			errors.Errorf("MCC(%s) found %+v != countryByName %+v", mcc, c, o)
		}
		if o, ok := countryByCC[cc]; ok {
			errors.Errorf("MCC(%s) found %+v != countryByCC %+v", mcc, c, o)
		}
		if o, ok := countryByISO[iso]; ok {
			errors.Errorf("MCC(%s) found %+v != countryByISO %+v", mcc, c, o)
		}

		//define the new country and add to all indices
		c = &Country{
			mcc:           record[0],
			nameList:      []string{cName},
			isoList:       []string{iso},
			ccList:        []string{cc},
			networkByMNC:  map[string]*Network{},
			networkByName: map[string]*Network{},
		}
		countryByCC[cc] = c
		countryByMCC[mcc] = c
		countryByName[cName] = c
		countryByISO[iso] = c
	} else {
		//existing country, add only if found another name/cc/iso
		c.nameList = appendIfNew(c.nameList, cName)
		c.ccList = appendIfNew(c.ccList, cc)
		c.isoList = appendIfNew(c.isoList, iso)
	}

	if n, ok := c.networkByMNC[mnc]; ok {
		//found existing network in this country already using this mnc
		//some small networks share the MNC, only name differ
		//e.g.
		// 722,1826,299,665,ar,Argentina,54,Imowi
		// 722,1826,299,665,ar,Argentina,54,IPLAN
		// 722,1826,299,665,ar,Argentina,54,Kallofer
		//it is allowed, then as long as all the other info matches
		//we just append the name to the network description
		n.nameList = appendIfNew(n.nameList, nName)
		c.networkByName[nName] = n
	} else {
		//new MNC
		//network name may already exist when this is another MNC for the same network
		n, ok := c.networkByName[nName]
		if ok {
			n.mncList = append(n.mncList, record[2])
		} else {
			n = &Network{
				Country:  c,
				nameList: []string{nName},
				mncList:  []string{mnc},
			}
			c.networkByName[record[7]] = n
		}
		c.networkByMNC[mnc] = n
	}
	return nil
} //LoadCSV()

func appendIfNew(list []string, s string) []string {
	for _, ss := range list {
		if ss == s {
			return list
		}
	}
	return append(list, s)
}

