type Network struct {
	Country  *Country
	nameList []string //multiple small networks can share a MNC
	mncList  []string //networks can owm multiple MNC e.g. South Africa MTN has 655 10 and 12
}

func (n Network) HasName(name string) bool {
	for _, cn := range n.nameList {
		if name == cn {
			return true
		}
	}
	return false
}

